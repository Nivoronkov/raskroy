"""
Метаданные приложения: версия, дата сборки, автор.

Используются в окне «О программе» и в заголовке. Дату сборки удобно обновлять
перед каждой сборкой .exe (можно вручную или скриптом).
"""

APP_NAME = "Калькулятор линейного раскроя"
APP_VERSION = "1.1.0"
BUILD_DATE = "2026-06-04"   # дата последней сборки/обновления — обновлять при релизе
AUTHOR = "Воронков Н.А."


def short_caption() -> str:
    """Короткая подпись для статусбара."""
    return f"{APP_NAME}  v{APP_VERSION}   •   Автор: {AUTHOR}"


def about_text() -> str:
    """Текст для окна «О программе»."""
    return (
        f"{APP_NAME}\n\n"
        f"Версия: {APP_VERSION}\n"
        f"Дата сборки: {BUILD_DATE}\n"
        f"Автор: {AUTHOR}\n\n"
        "Расчёт оптимального раскроя линейного проката по спецификации Компас."
    )
